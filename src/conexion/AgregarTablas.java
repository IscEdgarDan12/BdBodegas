    private void grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grabarActionPerformed
        try {
            FileInputStream archivofoto;
            archivofoto = new FileInputStream(ruta.getText());
            PreparedStatement pst = cn.prepareStatement("INSERT INTO productos (idProducto, codPro, desPro, prePro, NomImagen, imagen) VALUES (NULL,?,?,?,?,?)");
            pst.setString(1, txtCod.getText());
            pst.setString(2, txtDes.getText());
            pst.setString(3, txtPre.getText());
            pst.setString(4, ruta.getText());
            pst.setBinaryStream(5, archivofoto);
            pst.executeUpdate();
            mostarDatos(buscarId.getText());
        } catch (Exception e) {
            System.out.println("Error en insertat " + e.getMessage());
        }

    }//GEN-LAST:event_grabarActionPerformed

