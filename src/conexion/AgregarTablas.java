    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int filas = tbProductos.getSelectedRow();
        System.out.println("valor filas: " + filas);
        if (filas >= 0) {
            id.setText(tbProductos.getValueAt(filas, 0).toString());
            txtCod.setText(tbProductos.getValueAt(filas, 1).toString());
            txtDes.setText(tbProductos.getValueAt(filas, 2).toString());
            txtPre.setText(tbProductos.getValueAt(filas, 3).toString());
            String ruta2 = (tbProductos.getValueAt(filas, 4).toString());
            ruta.setText(ruta2);
            System.out.println(ruta2);
            Image foto = getToolkit().getImage(ruta2);
            foto = foto.getScaledInstance(110, 110, 1);
            imagen.setIcon(new ImageIcon(foto));
        } else {
            JOptionPane.showMessageDialog(null, "No hay filas seleccionadas");
        }
        id.setEnabled(false);
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        int fila = tbProductos.getSelectedRow();
        String idEliminar = "";
        idEliminar = tbProductos.getValueAt(fila, 0).toString();
        try {
            PreparedStatement pst = cn.prepareStatement("DELETE FROM `productos` WHERE `idProducto` =" + idEliminar + "");
            pst.executeUpdate();
            mostarDatos("");
        } catch (Exception e) {
            System.out.println("Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        try {
            FileInputStream archivofoto;
            File nombre = new File(ruta.getText());
            archivofoto = new FileInputStream(nombre);
            String update = "UPDATE productos SET"
                    + " codPro='" + txtCod.getText()
                    + "', desPro='" + String.valueOf(txtDes.getText())
                    + "', prePro='" + txtPre.getText() + "', NomImagen=?, imagen=? WHERE idProducto=" + id.getText() + "";
            System.out.println(update);
            PreparedStatement pst = cn.prepareStatement(update);
            archivofoto = new FileInputStream(ruta.getText());
            pst.setString(1, ruta.getText());
            pst.setBinaryStream(2, archivofoto);
            pst.executeUpdate();
            mostarDatos("");
        } catch (Exception e) {
            System.out.println("Error al actualizar " + e.getMessage());
        }
        limpiar();
        id.setEnabled(true);
    }//GEN-LAST:event_actualizarActionPerformed

    private void seleccionaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionaActionPerformed
        JFileChooser archivo = new JFileChooser();
        int ventana = archivo.showOpenDialog(null);

        if (ventana == JFileChooser.APPROVE_OPTION) {
            File file = archivo.getSelectedFile();
            ruta.setText(String.valueOf(file));
            Image foto = getToolkit().getImage(ruta.getText());
            foto = foto.getScaledInstance(110, 110, Image.SCALE_DEFAULT);
            imagen.setIcon(new ImageIcon(foto));

        }
    }//GEN-LAST:event_seleccionaActionPerformed
